def get_process_route(process: str) -> list[dict[str, str]]:
    if process not in ROUTES:
        supported = ", ".join(ROUTES)
        raise ValueError(f"Unsupported process route: {process}. Supported: {supported}")
    return ROUTES[process]


def route_names() -> list[str]:
    return list(ROUTES)
